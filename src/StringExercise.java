import java.util.Scanner;


public class StringExercise {
    public static void main(String[] args) {
        String twoSentances= "We don't need no education" +  '\n';
        twoSentances = twoSentances + "We don't need no thought control";
        System.out.println(twoSentances);

        String nextString = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(nextString);

        String anotherStr = "In windows, the main drive is usually C:\\";
        System.out.println(anotherStr);

        String anotherTwoSentances = "I can do backslashes \\, double backslashes \\\\," +  '\n';
        anotherTwoSentances = anotherTwoSentances + "and the amazing triple backslash \\\\\\!l";
        System.out.println(anotherTwoSentances);
    }
}


